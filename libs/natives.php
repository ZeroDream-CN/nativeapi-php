function ExecuteNative($native, $args) {
    $data = CallBridge('executeNative', [
        'name' => $native,
        'args' => $args
    ]);
    if ($data !== null) {
        if ($data['type'] == 'vector3') {
            return new Vector3($data['result'][0], $data['result'][1], $data['result'][2]);
        } else {
            return $data['result'];
        }
    } else {
        return null;
    }
}

foreach ($nativeList as $native) {
    $code  = "function $native() { return ExecuteNative('$native', func_get_args()); }";
    eval($code);
}
